package main

import "fmt"

func main(){

	var a = "intial"
	fmt.Println(a)

	var b, c int = 1,2
	fmt.Println(b, c)

	var d = true
	fmt.Println(d)

	var e int
	fmt.Println(e)

	f := "apple"  
	fmt.Println(f)

	// := syntax is shorthand for declaring and initializing a variable
	// for var f string = "apple" in this case
	// This syntax is only available inside functions
}